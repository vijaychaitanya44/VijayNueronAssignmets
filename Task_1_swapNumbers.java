package com.assignment1;

public class Task_1_swapNumbers {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		int c;
		System.out.println("Before values a=" + a + "\nBefore values b=" + b);
		c = a;
		a = b;
		b = c;

		System.out.println("Swapped values a=" + a + "\nSwapped values b=" + b);

	}

}
